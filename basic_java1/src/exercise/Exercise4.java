package exercise;

public class Exercise4 {

    public static void main(String[] args) {

        // 59.99
        // 30%할인된 가격을 출력하시오

        final double PRICE = 59.99;
        final double DISCOUNTRATE = 0.3;

        // 할인된 가격
        double Discoutprice = PRICE * DISCOUNTRATE;
        System.out.println(Discoutprice);

        // 정수로 형변환
        System.out.println((int)Discoutprice);

        //정수로 변환된 최종 가격
        System.out.println((int)Discoutprice);

    } // end of main

} // end of class
